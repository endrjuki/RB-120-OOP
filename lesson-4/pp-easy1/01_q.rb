=begin
Question 1
Which of the following are objects in Ruby? If they are objects, how can you find out what class they belong to?

1. true
2. "hello"
3. [1, 2, 3, "happy days"]
4. 142
=end

=begin
1) `true` is an object of `TrueClass` class
2) "hello" is an object of `String` class
3) `[1, 2, 3, "happy days"]` is an object of `Array` class
4) `142` is an object of `Integer` class

So all of them are objects, almost everything in Ruby is.
You can find out their class by calling class method `class` oon the object in question.
=end

